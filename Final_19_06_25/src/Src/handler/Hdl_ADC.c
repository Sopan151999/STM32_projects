void Hdl_Enable_ADC0_PA03(void)
{
    // Initialization code
    ADC0_Enable();
}
void Hdl_channel_select(uint16_t lclchannel){
    ADC0_REGS->ADC_INPUTCTRL = (uint16_t) lclchannel;
}
uint16_t Hdl_ADC0_PA3_result (uint16_t lclchannel)
{uint16_t lcl_count=0;
    // Periodic or polling logic
    Hdl_channel_select(lclchannel);
    ADC0_ConversionStart();
    while( !ADC0_ConversionStatusGet()){}
    
    lcl_count=ADC0_ConversionResultGet(); 
    
    return lcl_count;
}

